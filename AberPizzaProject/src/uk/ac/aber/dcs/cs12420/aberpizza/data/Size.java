package uk.ac.aber.dcs.cs12420.aberpizza.data;

/**
 * This is a enumerated class. This class specifies the different sizes that are
 * allowed for pizza.
 *
 * @author Kit
 */
public enum Size {

    /**
     * This is used for small pizzas.
     */
    SMALL,
    /**
     * This is used for medium pizzas.
     */
    MEDIUM,
    /**
     * This is used for large pizzas.
     */
    LARGE
}
